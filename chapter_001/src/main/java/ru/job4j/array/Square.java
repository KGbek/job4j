package ru.job4j.array;

public class Square {
    public int[] calculate(int bound) {
        int[] rst = new int[bound];
        for (int i = 0; i < bound; i++) {
            for (int j = 1; j < rst.length; j++) {
                return rst;
            }
            return rst;
        }
        return rst;
    }
}
